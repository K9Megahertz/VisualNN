#include "vertexarray.h"


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  CTORs/DTORs
//
//
//
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

VertexArray::VertexArray() {
	
	glGenVertexArrays(1, &m_id);
	glBindVertexArray(m_id);
	Logger::Append(Logger::LogLevel::LOGLEVEL_INFO, "Generated Vertex Array with id: " + std::to_string(m_id));

}

VertexArray::~VertexArray() {

	Logger::Append(Logger::LogLevel::LOGLEVEL_INFO, "  Deleting Vertex Array with id: " + std::to_string(m_id));
	glDeleteVertexArrays(1, &m_id);

}


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function bind()
//
//
//
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void VertexArray::bind() {

	glBindVertexArray(m_id);

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function unbind()
//
//
//
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void VertexArray::unbind() {

	glBindVertexArray(0);

}


